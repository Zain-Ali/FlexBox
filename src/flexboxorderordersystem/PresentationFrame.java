    /**
     * @param cardGrade
     * @param colourType
     * @param reinBottom
     * @param reinCorner
     * @return 
     */
    public int getBoxType(int cardGrade, int colourType, boolean reinBottom, boolean reinCorner) {
        int boxType = 0;

        for (BoxTypes box: boxes) {
            if(box.hasCardGrade (cardGrade) && box.getColourType() == colourType
                    && box.hasReinBottom() == reinBottom 
                    && box.hasReinCorner() == reinCorner) {
                boxType = box.getBoxType();
                break;
            }
        }
        
        return boxType;
    }
    
