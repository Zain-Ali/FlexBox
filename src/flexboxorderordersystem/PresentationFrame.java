    private void totalCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalCostActionPerformed
        try {
            int userW = Integer.parseInt(width.getText());
            int userH = Integer.parseInt(height.getText());
            int userL = Integer.parseInt(length.getText());
            int userQ = Integer.parseInt(quantity.getText());
            boolean userCG1 = boxType1.isSelected();
            boolean userCG2 = boxType2.isSelected();
            boolean userCG3 = boxType3.isSelected();
            boolean userCG4 = boxType4.isSelected();
            boolean userCG5 = boxType5.isSelected();
            int cardGrade;
            
            if(boxType1.isSelected())
                cardGrade = 1;
            else if(boxType2.isSelected())
                cardGrade = 2;
            else if(boxType3.isSelected())
                cardGrade = 3;
            else if (boxType4.isSelected())
                cardGrade = 4;
            else if (boxType5.isSelected())
                cardGrade = 5;
            else
                cardGrade = 0;    
            if(cardGrade == 0)
                JOptionPane.showMessageDialog (null, "Please select a grade of card");
            
            boolean userCT0 = colourType0.isSelected();
            boolean userCT1 = colourType1.isSelected();
            boolean userCT2 = colourType2.isSelected();
            
            int colourType = 0;
            
            if(colourType0.isSelected())
                colourType = 0;

            else if(colourType1.isSelected())
                colourType = 1;
            else if(colourType2.isSelected())
                colourType = 2;
            else
                JOptionPane.showMessageDialog (null, "Please select a colour type");
            
            boolean userRBYes = reinforcedBottomYes.isSelected();
            boolean userRBNo = reinforcedBottomNo.isSelected();
            
            boolean reinBottom = false;
            
            if(reinforcedBottomYes.isSelected())
                reinBottom = true;
            else if(reinforcedBottomNo.isSelected())
                reinBottom = false;
            else
                JOptionPane.showMessageDialog (null, "Please select a reinforced bottom");
            
            boolean userRCYes = reinforcedCornerYes.isSelected();
            boolean userRCNo = reinforcedCornerNo.isSelected();
            
            boolean reinCorner = false;
            
            if(reinforcedCornerYes.isSelected())
                reinCorner = true;
            else if(reinforcedCornerNo.isSelected())
                reinCorner = false;
            else
                JOptionPane.showMessageDialog(null, "Please select a reinforced corner");
            
            boolean userSTYes = sealTopYes.isSelected();
            boolean userSTNo = sealTopNo.isSelected();
            
            boolean sealTop = false;

            boolean isValidBoxOrder = true;
            
            if(sealTopYes.isSelected())
                sealTop = true;
            else if(sealTopNo.isSelected())
                sealTop = false;
            else {
                JOptionPane.showMessageDialog (null, "Please select a sealTop");
                isValidBoxOrder = false;
            }
            
            if(userW <5 || userW >500){
                JOptionPane.showMessageDialog (null, "Please enter a width betwen 5 and 500");
                isValidBoxOrder = false;
            }
            if(userH <5 || userH>500) {
                JOptionPane.showMessageDialog (null, "Please enter a height betwen 5 and 500");
                isValidBoxOrder = false;
            }
            if(userL <5 || userL>500) {
                JOptionPane.showMessageDialog (null, "Please enter a length betwen 5 and 500");
                isValidBoxOrder = false;
            }
            if(userQ<1){
                JOptionPane.showMessageDialog (null, "Please enter 1 or more quantity");
                isValidBoxOrder = false;
            }
            
            int boxType = getBoxType(cardGrade, colourType, reinBottom, reinCorner);
            
            if(boxType == 0) {
                JOptionPane.showMessageDialog (null, "Invalid box type."
                        + " We  do not supply this type of Boxes");
                isValidBoxOrder = false;
            }
      
            if(isValidBoxOrder) {
                PriceCalculation priceCalculation = new PriceCalculation();
                double area = priceCalculation.area(userW, userL, userH); //getting the box area
                double basePrice = priceCalculation.calculateBoxBasePrice(area, cardGrade);
                double additionalPrice = priceCalculation.calculatBoxAdditionalsPrice(basePrice, colourType, reinBottom, reinCorner, sealTop);
                TotalCostLabel.setText(String.format("£%.2f", additionalPrice*userQ)); 
                OrderShow1.setText(" W " + Integer.toString(userW) +" + H  " + Integer.toString(userH) + "+ L " +  Integer.toString(userL));
                quantityLabel3.setText(Integer.toString(userQ));
                      
                rbLabel.setText(Boolean.toString(userRBYes == true || userRBNo == false ));
                rcLabel.setText(Boolean.toString(userRCYes == true || userRCNo == false));
                stLabel.setText(Boolean.toString(userSTYes == true || userSTNo == false));
                
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                model.addRow(new Object[]{TotalCostLabel.getText(), quantityLabel3.getText(), 
                rbLabel.getText(), rcLabel.getText(), stLabel.getText()});
            }
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog (null, "invalid user input. Integer input required");              
        }                      

        
    }//GEN-LAST:event_totalCostActionPerformed

